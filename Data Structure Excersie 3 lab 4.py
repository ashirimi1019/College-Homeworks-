# Insertion Sort:

# Temporal Complexity:
# In the worst-case scenario, the time complexity of Insertion Sort is O(n^2),
# where 'n' denotes the quantity of elements within the array.
# This occurs when the array is arranged in reverse order.
# Conversely, in the best-case scenario, the time complexity is optimized to O(n)
# when the array is already sorted.
# The spatial complexity is O(1), indicating that it necessitates only a fixed amount
# of supplementary space for variables.

# Selection Sort:

# Temporal Complexity:
# The worst-case temporal complexity of Selection Sort is O(n^2),
# where 'n' symbolizes the number of elements in the array.
# Unlike insertion sort, the temporal complexity remains the same
# irrespective of the initial arrangement of elements.
# The spatial complexity is O(1), denoting that it utilizes a constant amount
# of space for variables.