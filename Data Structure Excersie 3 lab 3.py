# Space Complexity: O(1) - in-place sorting algorithm

# Counting Sort Analysis
# Time Complexity: O(n + k), where n is the number of elements and k is the input range
# Space Complexity: O(k) - due to an additional counting array

# Comparison:
# - Bubble Sort is suitable for small datasets or nearly sorted lists.
# - Counting Sort is preferred when the input range ('k') isn't significantly larger than the number of elements ('n'),
#   and when integers dominate the input. Memory usage is less of a concern.
